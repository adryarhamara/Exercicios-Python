#Programa que peça 2 números inteiros e um número real
n1 = int(input("Digite um numero inteiro: "))
n2 = int(input("Digite um segundo numero inteiro: "))
n3 = float(input("Digite um numero real: "))

print(f'Produto do dobro do {n1} com metade do {n2}: {(n1*n1)*(n2/2)} ')
print(f'Triplo do primeiro {n1} com o terceiro {n3}: {(n1*3)+n3}')
print(f'Terceiro elevado ao cubo {n3}: {n3**3}')
